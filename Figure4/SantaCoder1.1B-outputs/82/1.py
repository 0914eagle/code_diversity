def prime_length(string):
    
    if len(string) == 1:
        return True
    elif len(string) == 2:
        return False
    elif len(string) == 3:
        return True
    elif len(string) == 4:
        return False
    elif len(string) == 5:
        return True
    elif len(string) == 6:
        return False
    elif len(string) == 7:
        return True
    elif len(string) == 8:
        return False
    elif len(string) == 9:
        return True
    elif len(string) == 10:
        return False
    else:
        return False


