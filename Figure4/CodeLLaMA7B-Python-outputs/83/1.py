def starts_one_ends(n):
    
    # 10^n-10^(n-1) + 10^(n-1)
    # 10^n - 10^(n-1) + 1
    # 10^n - 10^(n-1) + 10^(n-1) - 10^(n-2) + 1
    # 10^n - 10^(n-2) + 1
    # 10^n - 10^(n-2) + 10^(n-2) - 10^(n-3) + 1
    # ...
    # 10^n - 10^1 + 1
    # 10^n - 1 + 1
    # 10^n
    return n * 10 ** (n - 1)


