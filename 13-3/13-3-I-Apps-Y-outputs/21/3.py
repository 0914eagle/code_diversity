
def create_staff(notes):
    staff = ["G:                                                            ",
             "F: -------------------------------------*-------------------- ",
             "E:       *   *          *             *   *                   ",
             "D: ----*-------**-----*-------------*-------*---------------- ",
             "C: * *     *      * *     **    * *           *         ** **",
             "B: --------------------------**-----------------*-----*------",
             "A:                                                  *         ",
             "g: -----------------------------------------------*----------",
             "f:                                                            ",
             "e: ----------------------------------------------------------",
             "d:                                                            ",
             "c:                                                            ",
             "b:                                                            ",
             "a: ----------------------------------------------------------"]

    for note in notes:
        pitch = note[0]
        duration = note[1] if len(note) == 2 else 1
        line = staff[ord(pitch) - ord("C")]
        line = line[:line.index("*")] + "*" * duration + line[line.index("*") + 1:]
        staff[ord(pitch) - ord("C")] = line

    return "\n".join(staff)

